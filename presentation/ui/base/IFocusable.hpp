/*
 * 文件名：IFocusable.hpp
 * 职责：可获得焦点的UI组件接口，定义焦点管理协议。
 * 依赖：无（基础接口）。
 * 线程：仅在UI线程使用。
 * 备注：用于键盘导航和可访问性支持。
 */

#pragma once

/// 可获得焦点的UI组件接口
/// 
/// 功能：
/// - 焦点状态查询：检查组件是否当前拥有焦点
/// - 焦点获取：程序化设置焦点到组件
/// - 焦点失去：响应焦点转移到其他组件
/// 
/// 使用场景：
/// - 键盘导航：Tab键在可焦点组件间切换
/// - 键盘操作：焦点组件响应键盘输入
/// - 可访问性：屏幕阅读器等辅助工具的焦点跟踪
/// - 视觉反馈：焦点组件显示焦点环或高亮状态
class IFocusable {
public:
	virtual ~IFocusable() = default;

	/// 功能：检查组件是否拥有焦点
	/// 返回：true表示组件当前拥有焦点，false表示没有焦点
	/// 说明：用于焦点状态查询和视觉状态更新
	virtual bool isFocused() const = 0;

	/// 功能：设置组件的焦点状态
	/// 参数：focused — true表示获得焦点，false表示失去焦点
	/// 说明：由焦点管理器调用，组件应更新内部状态和视觉反馈
	virtual void setFocused(bool focused) = 0;

	/// 功能：检查组件是否可以获得焦点
	/// 返回：true表示组件可以获得焦点，false表示不可获得（如禁用状态）
	/// 说明：用于焦点导航时跳过不可用的组件
	virtual bool canFocus() const = 0;
};